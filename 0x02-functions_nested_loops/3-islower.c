#include <stdlib.h>

/**
 * _islower - checks for characters in lowercase
 * @c: The character to be checked
 * Return: 1 if true else 0
 */
int _islower(int c)
{                                                       if (c >= 97)
        {
                return (1);
        }
        else
        {                                                       return (0);                             }
}
